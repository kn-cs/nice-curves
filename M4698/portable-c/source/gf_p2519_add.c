#include "basic_types.h"
#include "gf_p2519_type.h"
#include "gf_p2519_arith.h"

void gfp2519add(gfe_p2519_5L *h,const gfe_p2519_5L *f,const gfe_p2519_5L *g) {

	uchar8 i;
	uint64 t[NLIMBS+1];

	for (i=0; i<NLIMBS+1; ++i) {

		t[i] = f->l[i] + g->l[i]; 
		h->l[i] = t[i];
	}
}
